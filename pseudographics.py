ROW_HEIGHT = 8
SPACE = 5


# Функция для генерации псевдографики
def draw_text(text):
    text = text.upper()  # Приведение текста к верхнему регистру
    # Создаем пустой список, где каждая строка будет хранить соответствующую строку псевдографики
    lines = ["▒"] * ROW_HEIGHT

    # Проходим по каждой букве и добавляем соответствующие строки к каждой из 6 линий
    for char in text:
        if char in letters:
            for i in range(ROW_HEIGHT):
                lines[i] += letters[char][i]
        else:
            # Если символ не найден в словаре, добавляем пустую строку (или можно пробелы)
            for i in range(ROW_HEIGHT):
                lines[i] += "▒" * SPACE

    # Добавляем полосу из "▒" в конец каждой строки
    for i in range(ROW_HEIGHT):
        lines[i] += "▒" * SPACE

    # Выводим результат
    for line in lines:
        print(line)


if __name__ == "__main__":
    user_input = input("Введите текст: ")
    draw_text(user_input)
